package com.hanisha.primenumbers;

public class CalculationInheritance {
	int k;
	public void addition(int i,int j) {
		k = i + j;
		System.out.println(" Sum of given numbers :"+k);
	}
	public void subtraction(int i,int j) {
		k = i - j;
		System.out.println("Difference of given numbers : "+k);
	}
	public void multiplication(int i,int j) {
		k = i * j;
		System.out.println("Product of given numbers :"+k);
	}
	
}
