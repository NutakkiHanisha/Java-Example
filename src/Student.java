
public class Student {
		public static void main(String[] args) {
		      methodRankPoints(150);
		      
		}

		   public static void methodRankPoints(int points) {
		      if (points >= 160) {
		         System.out.println("Rank:A1");
		      }
		      else if (points >= 180) {
		         System.out.println("Rank:A2");
		      }
		      else {
		         System.out.println("Rank:A3");
		      }
		  
		   }

}
